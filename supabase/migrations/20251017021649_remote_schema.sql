CREATE OR REPLACE FUNCTION public.apply_discretionary_override(p_claim uuid, p_amount numeric, p_actor uuid)
 RETURNS void
 LANGUAGE plpgsql
 SECURITY DEFINER
AS $function$
begin
  if p_amount > 150000 then
    raise exception 'Discretionary override cannot exceed Ksh 150,000 as per Byelaws §6.1.';
  end if;

  update claims
